def build_prompt(responses):
    total = len(responses)
    correct = sum(1 for r in responses if r["is_correct"])
    wrong = total - correct
    score = round((correct / total) * 100, 2) if total > 0 else 0

    wrong_examples = [
        f'{r["word1"]} - {r["word2"]}'
        for r in responses if not r["is_correct"]
    ][:5]

    examples_text = "\n".join(wrong_examples) if wrong_examples else "لا يوجد"

    prompt = f"""
أنت أخصائي صعوبات تعلم متخصص في التمييز السمعي.

المهمة:
تحليل أداء الطفل اعتمادًا على النتيجة فقط.

قواعد صارمة:
- اكتب التقرير مرة واحدة فقط
- لا تكرر أي عنوان أو قسم
- لا تضف أي مقدمة أو خاتمة خارج الشكل المطلوب
- لا تكتب فقرات طويلة
- استخدم جمل قصيرة جداً وواضحة
- اكتب بلغة عربية بسيطة وسليمة
- لا تستخدم كلمات غريبة أو ترجمة حرفية
- لا تخترع أمثلة
- استخدم فقط أمثلة الأخطاء المعطاة
- لا تستخدم كلمة "تشخيص"
- استخدم فقط "مؤشر" أو "قد يشير"
- لا تترك أي قسم فارغ

البيانات:
- عدد الأسئلة: {total}
- الصحيحة: {correct}
- الأخطاء: {wrong}
- النسبة: {score}%

أمثلة الأخطاء:
{examples_text}

تحديد المستوى:
- 80% فأكثر: أداء قوي
- 60% فأكثر: أداء جيد
- 40% فأكثر: أداء متوسط
- أقل من 40%: أداء ضعيف

مهم:
- إذا كانت النسبة 60% أو أكثر → لا تذكر وجود صعوبات
- إذا كانت النسبة أقل من 40% → اذكر وجود مؤشر فقط
- إذا كانت النسبة بين 40% و59% → اذكر أن الأداء متوسط ويحتاج متابعة

اكتب التقرير بهذا الشكل فقط:

تقرير التمييز السمعي

التقييم العام:
- مستوى الأداء: (قوي أو جيد أو متوسط أو ضعيف)
- مؤشر صعوبات التمييز السمعي: (نعم أو لا مع سبب بسيط واضح)

تفاصيل الأداء:
- عدد الأسئلة: {total}
- الصحيحة: {correct}
- نسبة النجاح: {score}%

تحليل الأخطاء:
- مثال: صعوبة في التمييز بين الأصوات المتقاربة
- مثال: ضعف في الانتباه السمعي

التفسير التربوي:
- اكتب جملة قصيرة واضحة مثل:
  "الأداء متوسط ويشير إلى حاجة الطفل لتدريب إضافي في التمييز السمعي"

التوصيات:
- التدريب على التمييز بين الأصوات المتقاربة
- إعادة سماع الكلمات والتركيز على الفروقات
- استخدام ألعاب الاستماع والانتباه

ابدأ مباشرة بكلمة:
تقرير التمييز السمعي
"""
    return prompt