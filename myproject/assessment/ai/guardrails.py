import re

def sanitize_text(text):

    replacements = {
        r"Tقرير": "تقرير",
        r"صعود في التمييز": "صعوبة في التمييز",
        r"صعودة": "صعوبة",
        r"حدث مشكلة": "توجد صعوبة",
        r"الإعطاء": "التمييز",
        r"الصوتات": "الأصوات",
        r"الإعداد": "التدريب",
        r"التقاطع": "التمييز",
        r"الكذب": "الكلمة",
        r"النتيجات": "النتائج",
        r"قد يظهر لديه مؤشر مؤشر": "قد يظهر لديه مؤشر",
        r"لا سبب بسيط واضح": "بسبب الأخطاء في التمييز السمعي",
        r"مثال:": "",

        # 🔥 تصحيح تخبيص AI
        r"أغطيات": "أمثلة",
        r"الأطلاق": "الاستماع",
        r"الصIENTIFIC": "",
        r"الخدمة من المستشفيات": "",

        # 🔥 تحسين جملة المؤشر
        r"لا مع سبب بسيط واضح": "لا (لا توجد مؤشرات واضحة بناءً على النتيجة)",
    }

    # 🔁 استبدال الكلمات
    for pattern, replacement in replacements.items():
        text = re.sub(pattern, replacement, text)

    # 🔥 إزالة الاقتباسات
    text = re.sub(r"[\"“”]", "", text)

    # 🔥 إصلاح الأقواس (دمج الأسطر داخلها)
    text = re.sub(
        r"\(\s*([^\)]+?)\s*\)",
        lambda m: "(" + m.group(1).replace("\n", " ").strip() + ")",
        text
    )

    # 🔥 ترتيب الأقسام (كل قسم بسطر)
    for icon in ["📊", "📈", "🔍", "🎓", "💡", "⚠️"]:
        text = text.replace(icon, "\n" + icon)

    # 🔥 ترتيب bullet points
    text = re.sub(r"\s*-\s*", "\n- ", text)

    # 🔁 تنظيف تكرار الكلمات
    text = re.sub(r"(مؤشر\s+)+مؤشر", "مؤشر", text)

    # 🔥 تنظيف الأسطر الفارغة
    text = re.sub(r"\n+", "\n", text)

    # 🔥 تنظيف المسافات بدون تخريب الأسطر
    text = re.sub(r"[ \t]+", " ", text)

    return text.strip()